# [2] 구구단 전체출력 하되 출력형태는 아래와 같다
# 예)
#   2*1=2          3*1=3           4*1=4           5*1=5
#   2*2=2          3*2=6           4*2=8           5*2=10
#   2*3=2          3*3=9           4*3=12          5*3=15
#   2*4=2          3*4=12          4*4=16          5*4=20
#    ...
# 
#   6*1=2          7*1=7           8*1=8           9*1=9
#   6*2=12         7*2=14          8*2=16          9*2=18
#   6*3=18         7*3=21          8*3=24          9*3=27
#   6*4=24         7*4=28          8*4=32          9*4=36
#    ... 
for d2 in range(1,10):
    for d1 in range(2,6):
        if d1<5:print(f'{d1}*{d2}={d1*d2:2}',end='  ')
        else : print(f'{d1}*{d2}={d1*d2:2}')
for d2 in range(1,10):
    for d1 in range(6,10):
        if d1<9:print(f'{d1}*{d2}={d1*d2:2}',end='  ')
        else : print(f'{d1}*{d2}={d1*d2:2}')