# 사용자 정의 함수 - 실습
#--------------------------------------------------------------------------
# 함수 기능 : 원하는 단의 구구단을 출력해주는 기능 함수
# 함수 이름 : gugu
# 매개 변수 : 1개
# 함수 결과 : None
#--------------------------------------------------------------------------
def gugu(num1):
    for _ in range(1,10):
        print(f'{num1}*{_}={num1*_}')

gugu(5)


#--------------------------------------------------------------------------
# 함수 기능 : 파일의 확장자를 반환해주는 기능 함수
# 함수 이름 : hwak
# 매개 변수 : 1개
# 함수 결과 : str 확장자명
#--------------------------------------------------------------------------
def hwak(filename):
    f1=filename.split('.')
    return f1[-1]

print(hwak("artwork.jpg"))